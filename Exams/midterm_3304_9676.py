def canBalance(intArray):
  if len(intArray) == 1:
    return False

  for i in range(0, len(intArray)):
    leftList = intArray[:i]
    leftListOdd = []
    for number in leftList:
      if number % 2 != 0:
        leftListOdd.append(number)
    rightList = intArray[i:]
    rightListEven = []
    for number in rightList:
      if number % 2 == 0:
        rightListEven.append(number)
    if sum(leftListOdd) == sum(rightListEven):
      return True
  return False



print(canBalance([1, 1, 1, 2, 1, 1, 1]))
print(canBalance([2, 1, 5, 1, 1, 2, 3, 5]))
print(canBalance([2, 1, 5, 2, 3, 5]))
print(canBalance([10, 10]))