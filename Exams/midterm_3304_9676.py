def sum67(numbers):
  total = 0
  insideFlag = False
  for number in numbers:
    if number == 6:
      insideFlag = True
    elif number == 7:
      insideFlag = False
    elif insideFlag == True:
      total += number
  return total


print(sum67([1, 1, 6, 7, 2]))
